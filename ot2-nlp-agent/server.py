@app.post("/parse", response_model=ParseResponse)
def parse_instruction(request: ParseRequest):
    """Parse a natural language instruction."""
    intent = agent.parse(request.instruction)
    return ParseResponse(
        operation=intent.operation_type.value if intent.operation_type else None,
        params=intent.params,
        confidence=intent.confidence,
        language=intent.language
    )


@app.post("/protocol", response_model=ProtocolResponse)
def create_protocol(request: ProtocolRequest):
    """Create a complete protocol from configuration and instructions."""
    # Create protocol
    protocol = agent.create_protocol(
        name=request.name,
        description=request.description
    )

    # Add labware
    for lw in request.labware:
        agent.add_labware(protocol, lw.labware_type, lw.slot, lw.name)

    # Add pipettes
    for pip in request.pipettes:
        agent.add_pipette(protocol, pip.pipette_type, pip.mount, pip.tip_rack)

    # Add instructions
    for instruction in request.instructions:
        agent.add_instruction(protocol, instruction)

    # Validate
    validation = agent.validate(protocol)

    # Generate
    preview = agent.preview(protocol)
    code = agent.generate(protocol)

    return ProtocolResponse(
        preview=preview,
        validation=str(validation),
        is_valid=validation.is_valid,
        code=code
    )


