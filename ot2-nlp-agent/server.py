# ============ Planner/Compiler Endpoints ============

# Store last planning result for compile endpoint
_last_plan_output: Optional[PlannerOutput] = None


@app.post("/plan", response_model=PlanResponse)
def plan_experiment(request: PlanRequest):
    """
    Plan an experiment from a natural language intent.

    Returns candidate workflows with missing parameters that need to be filled.
    Use the /compile endpoint to compile a selected candidate.
    """
    global _last_plan_output

    try:
        output = planner.plan(request.intent, request.context)
        _last_plan_output = output

        # Convert to response models
        candidates = []
        for candidate in output.candidates:
            uos = []
            for uo in candidate.unit_operations:
                placeholders = [
                    MissingInfoResponse(
                        parameter=name,
                        question=info.question,
                        question_zh=info.question_zh,
                        options=info.options,
                        default=info.default,
                        required=info.required
                    )
                    for name, info in uo.placeholders.items()
                ]
                uos.append(UnitOperationResponse(
                    name=uo.name,
                    uo_type=uo.uo_type.value,
                    description=uo.description,
                    parameters=uo.parameters,
                    placeholders=placeholders
                ))

            candidates.append(WorkflowCandidateResponse(
                name=candidate.name,
                description=candidate.description,
                confidence=candidate.confidence,
                unit_operations=uos,
                assumptions=candidate.assumptions,
                alternatives=candidate.alternatives
            ))

        return PlanResponse(
            intent=IntentResponse(
                goal=output.intent.goal,
                domain=output.intent.domain,
                language=output.intent.language,
                confidence=output.intent.confidence,
                target_metrics=output.intent.target_metrics,
                known_conditions=output.intent.known_conditions
            ),
            candidates=candidates,
            recommended_idx=output.recommended_idx
        )

    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


@app.post("/compile", response_model=CompileResponse)
def compile_workflow(request: CompileRequest):
    """
    Compile a workflow candidate from a previous /plan call.

    Requires that /plan was called first to generate candidates.
    """
    global _last_plan_output

    if _last_plan_output is None:
        raise HTTPException(
            status_code=400,
            detail="No planning result available. Call /plan first."
        )

    if request.candidate_index < 0 or request.candidate_index >= len(_last_plan_output.candidates):
        raise HTTPException(
            status_code=400,
            detail=f"Invalid candidate_index. Must be 0-{len(_last_plan_output.candidates)-1}"
        )

    try:
        # Get the selected candidate
        candidate = _last_plan_output.candidates[request.candidate_index]

        # Create confirmed workflow
        confirmed = ConfirmedWorkflow(
            draft=candidate,
            filled_parameters=request.filled_parameters
        )

        # Compile
        output = compiler.compile(confirmed)

        # Convert validation issues
        issues = [
            ValidationIssueResponse(
                severity=issue.severity,
                message=issue.message,
                step=issue.step
            )
            for issue in output.validation_result.issues
        ]

        # Convert checkpoints
        checkpoints = [
            CheckpointResponse(
                step_index=cp.step_index,
                step_name=cp.step_name,
                message=cp.message,
                message_zh=cp.message_zh,
                is_critical=cp.is_critical
            )
            for cp in output.validation_result.checkpoints
        ]

        return CompileResponse(
            is_valid=output.validation_result.is_valid,
            error_count=output.validation_result.error_count,
            warning_count=output.validation_result.warning_count,
            issues=issues,
            checkpoints=checkpoints,
            python_code=output.python_code,
            workflow_json=output.workflow_json
        )

    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


@app.post("/plan-compile", response_model=CompileResponse)
def plan_and_compile(request: PlanAndCompileRequest):
    """
    Plan and compile an experiment in one step.

    Combines /plan and /compile for convenience.
    """
    try:
        # Plan
        output = planner.plan(request.intent, request.context)

        if not output.candidates:
            raise HTTPException(
                status_code=400,
                detail="No workflow candidates could be generated for this intent."
            )

        # Select candidate
        idx = request.candidate_index
        if idx < 0 or idx >= len(output.candidates):
            idx = output.recommended_idx

        candidate = output.candidates[idx]

        # Create confirmed workflow
        confirmed = ConfirmedWorkflow(
            draft=candidate,
            filled_parameters=request.parameters
        )

        # Compile
        compile_output = compiler.compile(confirmed)

        # Convert to response
        issues = [
            ValidationIssueResponse(
                severity=issue.severity,
                message=issue.message,
                step=issue.step
            )
            for issue in compile_output.validation_result.issues
        ]

        checkpoints = [
            CheckpointResponse(
                step_index=cp.step_index,
                step_name=cp.step_name,
                message=cp.message,
                message_zh=cp.message_zh,
                is_critical=cp.is_critical
            )
            for cp in compile_output.validation_result.checkpoints
        ]

        return CompileResponse(
            is_valid=compile_output.validation_result.is_valid,
            error_count=compile_output.validation_result.error_count,
            warning_count=compile_output.validation_result.warning_count,
            issues=issues,
            checkpoints=checkpoints,
            python_code=compile_output.python_code,
            workflow_json=compile_output.workflow_json
        )

    except HTTPException:
        raise
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


@app.get("/templates")
def list_templates():
    """List all available UO templates."""
    from ot2_agent.templates import get_template_registry

    registry = get_template_registry()
    templates = registry.list_templates()

    result = {}
    for name in templates:
        template = registry.get_template(name)
        if template:
            result[name] = {
                "uo_type": template.uo_type.value,
                "description": template.description,
                "parameters": list(template.parameters.keys()),
                "placeholders": list(template.placeholders.keys()),
            }

    return result


@app.get("/missing-parameters")
def get_missing_parameters(intent: str, context: Optional[str] = None):
    """
    Get the missing parameters for a given intent.

    Useful for building forms to collect user input.
    """
    try:
        ctx = {}
        if context:
            import json
            ctx = json.loads(context)

        output = planner.plan(intent, ctx)

        if not output.candidates:
            return {"missing_parameters": []}

        # Get missing params from recommended candidate
        candidate = output.candidates[output.recommended_idx]
        missing = []

        for uo in candidate.unit_operations:
            for name, info in uo.placeholders.items():
                missing.append({
                    "parameter": name,
                    "question": info.question,
                    "question_zh": info.question_zh,
                    "options": info.options,
                    "default": info.default,
                    "required": info.required,
                    "uo_name": uo.name
                })

        return {"missing_parameters": missing}

    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))


